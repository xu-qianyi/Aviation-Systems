package Person;

import User.Identifiable;

public abstract class PersonProfile implements Identifiable {
    protected String id;
    protected String name;
    protected String gender;
    protected int age;
    protected String email;

    public PersonProfile(String id, String name, String gender, int age, String email) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.age = age;
        this.email = email;
    }
    
    @Override
    public String getId() {
        return id;
    }
    
    public String getName() {
        return name;
    }

    public String getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }

    public String getEmail() {
        return email;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
    
}
